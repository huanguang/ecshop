	“天价鱼”饭店生意如常 顾客称万元鳇鱼不奇怪
</p>
<div class="blank5" style="margin:0px auto;padding:0px;border:0px;font-size:1px;">
</div>
<p class="lh30 left f14 yahei" style="font-size:14px;font-family:'Microsoft YaHei';">
	2016-02-17 14:14:00 来源：<a href="http://news.cnr.cn/native/gd/20160217/t20160217_521398451.shtml" target="_blank">中国经济网</a> 
</p>
<div class="blank8" style="margin:0px auto;padding:0px;border:0px;font-size:1px;">
</div>
