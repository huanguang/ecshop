	<div class="f16 lh32 yahei left" style="margin:0px;padding:0px;border:0px;font-size:16px;font-family:'Microsoft YaHei';color:#242424;background-color:#FFFFFF;">
		<div class="TRS_Editor" style="margin:0px;padding:0px;border:0px;">
			<p align="center">
				<img alt="" src="http://news.cnr.cn/native/gd/20160217/W020160217512850519380.jpg" /> 
			</p>
			<p align="center" class="pictext">
				　　图为网络热炒的北岸野生鱼村大鳇鱼。中国经济网记者黄力辉 摄
			</p>
			<p>
				　　中国经济网哈尔滨2月17日讯（记者倪伟龄 黄力辉）网曝哈尔滨“万元铁锅炖”事件后，始作俑者——北岸野生渔村成为市民街谈巷议焦点。2月17日下午，记者来到这家饭店，了解事件进展。
			</p>
			<p>
				　　从松花江畔著名的太阳岛雪博会、冰雪大世界景区到此只需5分钟车程。饭店位于松北区松北大道，地处哈尔滨市政府、松北区政府中间的转盘道旁。再向前还可抵达东北虎林园、哈尔滨音乐厅等著名旅游文化景点。确是得天独厚上风上水之地。饭店背靠松北名盘“新新怡园”，往来政、商、旅游客流不断，占此宝地经营东北野生江鱼美食，真可谓占尽市场先机。即便“天价鱼”风波尚未平息，但记者见到北岸野生渔村门前的停车场依旧一位难求。渔村的中式门面雕梁画檐。墙上挂满了红辣椒，加上六盏红灯笼和两个倒置的红福字，体现浓郁北方民俗。
			</p>
			<p align="center">
				<img alt="" src="http://news.cnr.cn/native/gd/20160217/W020160217512851023848.jpg" /> 
			</p>
			<p align="center" class="pictext">
				　　图为北岸野生鱼村门面装修体现浓郁北方民俗。中国经济网记者黄力辉 摄
			</p>
			<p>
				　　步入厅堂，可见各面墙上张贴着醒目的宣传海报，着重介绍松花江江鱼的品种、口味，以及北岸野生渔村特色主打菜：铁锅炖鳇鱼、山野菜、三烩一焖、农家特色鱼冻。边走边看，记者发现“传统东北火炕、地道东北老味、正宗赫哲做法”是该渔村最拿手的得意之作，已助其稳坐宝地3年之久。
			</p>
			<p>
				　　未到晚间饭口，已有多位身着红色中式服装的男女服务员和身着白衣的厨师出出进进忙个不停。一位厨师指着水池中的活鱼向一位男服务员交代着晚间订席的用料。记者见到池中大大小小的江鱼缓缓游动。鱼池旁的冰鲜池上，十多条各色江鱼平躺在碎冰屑中等待食客挑选。其中有一条最大的鱼只剩后半段，便是那现已火遍大江南北的东北野生大鳇鱼。虽只半条，看上去也有二三十斤，价值上万元。
			</p>
			<p>
				　　鳇鱼为什么名贵？中国经济网记者了解到，鳇鱼是侏罗纪的物种，距今2亿年。过去是皇家贡品，肉质细腻，骨软无刺，口味鲜美，贵为“鱼中之皇”，清朝皇室赐名“鳇鱼”。这种珍稀冷水鱼种的生长期比南方鱼种多一倍还多，被誉为“水中熊猫”。记者留意到，店堂内有块一人高的价格公示板，用红底黄字标示出每种鱼的价格。其中最贵的鳇鱼每斤398元。最便宜的不到百元。记者发现，除大鳇鱼这类珍馐美味外。大量菜品价格还都不难接受。花三五十元也能饱餐一顿。
			</p>
			<p>
				　　记者向工作人员表明希望面见老板采访，服务员说老板和老板娘都不在，这两天身体不好，正在医院打针。记者希望其他前台经理能来谈谈情况。服务员说只能由老板接待。记者索要到饭店老板手机，多次拨打，直至发稿时止，无人接听。
			</p>
			<p>
				　　记者得知，2月15日晚，哈尔滨市松北区已就此次“万元铁锅炖”消费争端公布了调查情况通报，认定渔村大堂内设有相关食材价格公示，属明码标价，其中鳇鱼价格属于经营者自主制定的市场调节价，不在政府指导价和政府定价范畴之内。当日饭店方向消费者陈某告知其所点食材价格，陈某并无异议。关于媒体关注的鳇鱼价格199元/公斤是否存在违规问题，按照《价格法》第一章第三条关于“大多数商品和服务价格实行市场调节价，极少数商品和服务价格实行政府指导价或者政府定价”及第一章第十一条关于“经营者进行价格活动，享有自主制定属于市场调节的价格的权利”等规定，鳇鱼销售价格不在政府指导价和政府定价范畴之内，属于经营者自主制定的市场调节价。饭店方在总金额(10302元)基础上给予七折优惠，实收7200元。
			</p>
			<p>
				　　中国经济网记者询问服务员饭店经营是否受到舆论风波影响，答复没太大变化。记者随机采访来往客人，有观点认为，东北野生大鳇鱼和长江刀鱼一样，身价过万并不奇怪，这与青岛天价大虾事件不同。年节期间，饭店价格比市场价格高2倍也算常见。此事目前仍在不断发酵，很多细节值得推敲。但毕竟旅游为了高兴，经商和气生财，剧情演绎至此实在令人遗憾。
			</p>
